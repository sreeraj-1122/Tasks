// 1. Write an algorithm,flowchart and program to check whether the given number is even or odd.


//Program

const oddOrEven = (number) => {
    if (number % 2 === 0) {
        console.log(`${number} is even number`);
    } else {
        console.log(`${number} is odd number`);

    }
}

oddOrEven(2);
oddOrEven(13);


// Algorithm

// step 1: start
// step 2: Read the number
// step 3: if number is divisible by 2 
//             print number is even
// step 4: else 
//             print number is odd
// step 5: stop



